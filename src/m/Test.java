    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jButton2.isEnabled()) {
            checkAsnwer();
        }
        buttonGroup1.clearSelection();

        currentQuestionNum++;

        //stop if no more questions
        if (currentQuestionNum
            >= this.questionList.getQuestions().size()) {
            jButton1.setEnabled(false);

            Enumeration arr = buttonGroup1.getElements();
            while (arr.hasMoreElements()) {
                JRadioButton btn = (JRadioButton) arr.nextElement();
                btn.setEnabled(false);
            }
            jButton2.setEnabled(false);
            return;
        }

        Enumeration arr = buttonGroup1.getElements();
        while (arr.hasMoreElements()) {
            JRadioButton btn = (JRadioButton) arr.nextElement();
            btn.setEnabled(true);
        }
        jButton2.setEnabled(true);

        currentQuestion = questionList.getQuestions().get(currentQuestionNum);
        displayQuestion(currentQuestion);

    }//GEN-LAST:event_jButton1ActionPerformed

