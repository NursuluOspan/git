     public void checkAsnwer() {
        Enumeration arr = buttonGroup1.getElements();
        boolean flag = false;
        while (arr.hasMoreElements()) {
            JRadioButton btn = (JRadioButton) arr.nextElement();
            if (btn.getText().equals(
                    currentQuestion.getCorrectAnswer())
                    && btn.isSelected()) {
                flag = true;
                break;
            }
        }

        if (flag) {
            score++;
            
        } else {
        }
    }
    
